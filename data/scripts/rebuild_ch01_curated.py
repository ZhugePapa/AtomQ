def downgrade_headings(md: str) -> str:
    lines = []
    for line in md.splitlines():
        stripped = line.lstrip()
        indent = line[: len(line) - len(stripped)]
        if stripped.startswith("#"):
            hashes, _, rest = stripped.partition(" ")
            if hashes and set(hashes) == {"#"} and 1 <= len(hashes) < 6:
                lines.append(f"{indent}{hashes}# {rest}")
                continue
        lines.append(line)
    return "\n".join(lines)


def wrap_unhighlighted(text: str, needle: str, replacement: str) -> str:
    parts = text.split("==")
    for index in range(0, len(parts), 2):
        parts[index] = parts[index].replace(needle, replacement)
    return "==".join(parts)


def highlight_structured_terms(md: str, point_id: str) -> str:
    terms = sorted(HIGHLIGHT_TERMS.get(point_id, []), key=len, reverse=True)
    if not terms:
        return md

    out = []
    for line in md.splitlines():
        if line.strip().startswith("<!--") or line.strip().startswith("|:"):
            out.append(line)
            continue

        updated = line
        if "|" in updated and updated.strip().startswith("|"):
            cells = updated.split("|")
            for idx, cell in enumerate(cells):
                stripped = cell.strip()
                if stripped in terms:
                    cells[idx] = cell.replace(stripped, f"=={stripped}==")
            updated = "|".join(cells)

        for term in terms:
            updated = wrap_unhighlighted(updated, f"{term}：", f"=={term}==：")
            updated = wrap_unhighlighted(updated, f"{term}:", f"=={term}==:")

        out.append(updated)
    return "\n".join(out)


def render_markdown(card: dict) -> str:
    md = card["md"].strip()
    md = highlight_structured_terms(md, card["point_id"])
    md = downgrade_headings(md)
    return md + "\n"


def sha256(path: Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest()


def write_json(path: Path, payload) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def estimated_seconds(md: str) -> int:
    chars = len(md.replace("\n", ""))
    return max(75, min(260, round(chars / 6)))


def card_meta(card: dict, sort_no: int) -> dict:
    filename_base = f"{CHAPTER_ID}_{card['section_id']}_{card['point_id']}"
    md = render_markdown(card)
    field_override = FIELD_OVERRIDES.get(card["point_id"], {})
    return {
        "point_id": card["point_id"],
        "subject_id": SUBJECT_ID,
        "chapter_id": CHAPTER_ID,
        "section_id": card["section_id"],
        "title": card["title"],
        "card_type": card["card_type"],
        "difficulty": card["difficulty"],
        "estimated_read_seconds": estimated_seconds(md),
        "has_key_content": "==" in md,
        "is_free": True,
        "sort_no": sort_no,
        "content_file": f"{filename_base}.md",
        "content_md_path": f"subjects/{SUBJECT_ID}/chapters/{CHAPTER_ID}/cards/{filename_base}.md",
        "tags": card["tags"],
        "prerequisite_point_ids": [],
        "related_point_ids": [],
        "related_question_ids": [],
        "key_points": field_override.get("key_points", card["key_points"]),
        "mnemonics": field_override.get("mnemonics", card["mnemonics"]),
        "common_mistakes": [],
        "review_status": "ai_draft",
        "source_refs": [
            {
                "source": "2026新版高项三色笔记（信息系统项目管理师）",
                "location": f"拆分PDF 01 第{page}页",
            }
            for page in card["source_pages"]
        ],
    }


def rebuild_file_index() -> None:
    files = []
    for path in sorted(PUBLIC_ROOT.rglob("*")):
        if path.is_file() and path.name != "file_index.json":
            files.append(
                {
                    "path": path.relative_to(PUBLIC_ROOT).as_posix(),
                    "bytes": path.stat().st_size,
                    "sha256": sha256(path),
                }
            )
    write_json(
        PUBLIC_ROOT / "file_index.json",
        {
            "package_id": "atomq_high_itpmp_2026_public_full",
            "content_version": "2026.05.full-ocr-draft.1.public-full",
            "file_count": len(files),
            "files": files,
        },
    )


def main() -> None:
    if CARDS_DIR.exists():
        shutil.rmtree(CARDS_DIR)
    CARDS_DIR.mkdir(parents=True, exist_ok=True)

    for idx, card in enumerate(CARDS, start=1):
        base = f"{CHAPTER_ID}_{card['section_id']}_{card['point_id']}"
        write_json(CARDS_DIR / f"{base}.json", card_meta(card, idx))
        (CARDS_DIR / f"{base}.md").write_text(render_markdown(card), encoding="utf-8")

    counts = {section["section_id"]: 0 for section in SECTIONS}
    for card in CARDS:
        counts[card["section_id"]] += 1
    sections = [{**section, "card_count": counts[section["section_id"]]} for section in SECTIONS]

    write_json(
        CHAPTER_ROOT / "chapter_meta.json",
        {
            "chapter_id": CHAPTER_ID,
            "subject_id": SUBJECT_ID,
            "title": "信息化发展",
            "description": "信息与信息化基础、现代化基础设施、现代化创新发展、数字中国、数字化转型与元宇宙。",
            "sort_no": 1,
            "is_free": True,
            "card_count": len(CARDS),
            "question_count": 7,
            "sections": sections,
            "schema_version": 1,
        },
    )

    subject_index_path = SUBJECT_ROOT / "subject_index.json"
    subject_index = json.loads(subject_index_path.read_text(encoding="utf-8"))
    for chapter in subject_index["chapters"]:
        if chapter["chapter_id"] == CHAPTER_ID:
            chapter["card_count"] = len(CARDS)
            chapter["question_count"] = 7
    write_json(subject_index_path, subject_index)

    manifest_path = PUBLIC_ROOT / "manifest.json"
    manifest = json.loads(manifest_path.read_text(encoding="utf-8"))
    for chapter in manifest.get("chapters", []):
        if chapter["chapter_id"] == CHAPTER_ID:
            chapter["card_count"] = len(CARDS)
            chapter["question_count"] = 7
    write_json(manifest_path, manifest)

    questions_path = SUBJECT_ROOT / "questions" / "ch_01_questions.json"
    questions = json.loads(questions_path.read_text(encoding="utf-8"))
    for question in questions:
        target = QUESTION_REF_UPDATES.get(question["question_id"])
        if target:
            question["knowledge_point_ids"] = [{"point_id": target, "is_primary": True}]
    write_json(questions_path, questions)

    rebuild_file_index()
    print(f"Rebuilt {CHAPTER_ID}: {len(SECTIONS)} sections, {len(CARDS)} curated cards")
    for section in sections:
        print(f"- {section['section_id']} {section['title']}: {section['card_count']}")


if __name__ == "__main__":
    main()
