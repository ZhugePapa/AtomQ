def load_json(path: Path) -> dict:
    return json.loads(path.read_text(encoding="utf-8"))


def write_json(path: Path, payload: dict) -> None:
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def sha256(path: Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest()


def rebuild_file_index() -> None:
    entries = []
    for path in sorted(PUBLIC_ROOT.rglob("*")):
        if path.is_file() and path.name != "file_index.json":
            entries.append(
                {
                    "path": path.relative_to(PUBLIC_ROOT).as_posix(),
                    "bytes": path.stat().st_size,
                    "sha256": sha256(path),
                }
            )
    write_json(
        PUBLIC_ROOT / "file_index.json",
        {
            "package_id": "atomq_public_content",
            "version": "2026.1.0",
            "file_count": len(entries),
            "files": entries,
        },
    )


def main() -> None:
    changed_md = 0
    changed_json = 0

    for json_path in sorted(CARDS_DIR.glob("*.json")):
        stem = json_path.stem
        md_path = json_path.with_suffix(".md")
        if stem in MD_OVERRIDES:
            md_path.write_text(MD_OVERRIDES[stem].rstrip() + "\n", encoding="utf-8")
            changed_md += 1

        data = load_json(json_path)
        old = json.dumps(data, ensure_ascii=False, sort_keys=True)
        data["source_refs"] = [{"source_id": SOURCE_ID, "page_label": SOURCE_PAGES[stem]}]
        data["review_status"] = "ai_draft"
        if stem in META_OVERRIDES:
            data.update(META_OVERRIDES[stem])
        data["has_key_content"] = "==" in md_path.read_text(encoding="utf-8")
        data["estimated_read_seconds"] = max(60, min(300, len(md_path.read_text(encoding="utf-8")) // 4))
        new = json.dumps(data, ensure_ascii=False, sort_keys=True)
        if old != new:
            write_json(json_path, data)
            changed_json += 1

    rebuild_file_index()
    print(json.dumps({"changed_md": changed_md, "changed_json": changed_json}, ensure_ascii=False))


