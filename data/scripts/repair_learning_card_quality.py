def normalize_title_and_tags(card: dict) -> bool:
    changed = False
    title = card.get("title")
    if isinstance(title, str):
        cleaned = normalize_inline(title)
        if cleaned != title:
            card["title"] = cleaned
            changed = True
    tags = card.get("tags")
    if isinstance(tags, list):
        clean_tags: list[str] = []
        for tag in tags:
            if not isinstance(tag, str):
                continue
            tag = normalize_inline(tag)
            if not tag or tag in clean_tags:
                continue
            clean_tags.append(tag)
        if clean_tags != tags:
            card["tags"] = clean_tags
            changed = True
    return changed


def extract_highlight_key_points(md_text: str) -> str:
    seen: set[str] = set()
    items: list[str] = []
    for raw in re.findall(r"==([^=\n]{2,60})==", md_text):
        item = raw.strip(" ，。、；;：:")
        if not item or item in seen:
            continue
        if any(bad in item for bad in ("####", "Dedine", "Nerify", "Ven6y", "RIFD", "FAS)")):
            continue
        seen.add(item)
        items.append(f"- **{item}**")
        if len(items) >= 5:
            break
    return "\n".join(items)


