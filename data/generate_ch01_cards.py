#!/usr/bin/env python3
"""Generate Chapter 1 (信息化发展) knowledge cards — JSON + Markdown."""
import json, os, shutil
from datetime import datetime

BASE = os.path.join(os.path.dirname(os.path.abspath(__file__)), '知识点集合')
CH = 'ch_01'
CARDS_DIR = os.path.join(BASE, 'chapters', CH, 'cards')
os.makedirs(CARDS_DIR, exist_ok=True)

SUBJECT = 'subj_high_level'
NOW = '2026-05-10T00:00:00Z'

def write_json(point_id, chapter_id, section_id, title, card_type, difficulty, est_read,
               has_key, is_free, sort, content_file, tags,
               key_points, mnemonics,
               prereq, related, related_q, source, source_type):
    data = {
        "point_id": point_id,
        "subject_id": SUBJECT,
        "chapter_id": chapter_id,
        "section_id": section_id,
        "title": title,
        "card_type": card_type,
        "difficulty": difficulty,
        "estimated_read_seconds": est_read,
        "has_key_content": has_key,
        "is_free": is_free,
        "sort_no": sort,
        "content_file": content_file,
        "tags": tags,
        "key_points": key_points,
        "mnemonics": mnemonics,
        "prerequisite_point_ids": prereq,
        "related_point_ids": related,
        "related_question_ids": related_q,
        "source": source,
        "source_type": source_type,
        "author": "内容运营",
        "reviewer": "",
        "created_at": NOW,
        "updated_at": NOW,
        "version": 1,
        "schema_version": 1
    }
    path = os.path.join(CARDS_DIR, f'{point_id}.json')
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

def write_md(filename, content):
    path = os.path.join(CARDS_DIR, filename)
    with open(path, 'w', encoding='utf-8') as f:
        f.write(content)

def card(point_id, section_id, title, card_type, difficulty, est_read, has_key, is_free,
         sort, tags, key_points, mnemonics, prereq, related, related_q, source, source_type, md_content):
    cf = f'{point_id}.md'
    write_json(point_id, CH, section_id, title, card_type, difficulty, est_read, has_key,
               is_free, sort, cf, tags, key_points, mnemonics, prereq, related, related_q, source, source_type)
    write_md(cf, md_content)

